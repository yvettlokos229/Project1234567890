// 假设这是一个函数，用于计算两个数字的和
function addNumbers(a, b) {
    return a + b;
}

// 假设这是另一个函数，用于比较两个数字并返回较大的一个
function compareNumbers(num1, num2) {
    if (num1 > num2) {
        return true;
    } else {
        return false;
    }
}

// 调用上述函数以计算和与较大的数的比值
const result = addNumbers(5, 3);
console.log(`两个数字的和是: ${result}`);
